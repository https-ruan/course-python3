"""

Conversão de tipos, coerção
type convertion, typecasting, coercion

é o ato de converter um tipo em outro
tipos imutáveis e primitivos:
str, int, float, bool

"""

print('1' + str(1)) 
print(int('1') + 1) 
print(1 + 1.0) # implicity convertion
print(float(1) + 1.0)
print(bool(0), bool(1))
print(bool(''), bool('a'))