"""
Python = Linguagem de programação
Tipagem = Dinâmica / Forte
str -> string -> texto
Strings são textos que estão dentro de aspas
"""
 
# Aspas simples
print('Ruan Araujo')
print('Ruan "Araujo"') # Para melhor leitura

# Aspas duplas
print("Ruan Araujo")
print("Ruan 'Araujo'") # Para melhor leitura

# Escape -> \
print("Ruan \"Araujo\"")

# r
print(r"Ruan \"Araujo\"")